public class Video {
    private int v_id;
    private int v_userid;
    private String v_name;
    private String v_src_path;

    public int getV_id() {
        return v_id;
    }

    public void setV_id(int v_id) {
        this.v_id = v_id;
    }

    public int getV_userid() {
        return v_userid;
    }

    public void setV_userid(int v_userid) {
        this.v_userid = v_userid;
    }

    public String getV_name() {
        return v_name;
    }

    public void setV_name(String v_name) {
        this.v_name = v_name;
    }

    public String getV_src_path() {
        return v_src_path;
    }

    public void setV_src_path(String v_src_path) {
        this.v_src_path = v_src_path;
    }
}
